function resolveInitialLocale(): string {
  if (typeof window === 'undefined') {
    return 'th';
  }

  const savedLocale = window.localStorage.getItem(LOCALE_STORAGE_KEY);
  if (savedLocale && translations[savedLocale]) {
    return savedLocale;
  }

  const browserLanguage = window.navigator.language?.toLowerCase() ?? '';
  return browserLanguage.startsWith('en') ? 'en' : 'th';
}

let currentLocale = resolveInitialLocale();

if (typeof document !== 'undefined') {
  document.documentElement.lang = currentLocale;
}

export const i18nProvider: I18nProvider = {
  translate: (key: string, _params?: unknown, defaultMessage?: string) => {
    return translations[currentLocale]?.[key] ?? defaultMessage ?? key;
  },
  changeLocale: async (lang: string) => {
    currentLocale = translations[lang] ? lang : 'th';

    if (typeof window !== 'undefined') {
      window.localStorage.setItem(LOCALE_STORAGE_KEY, currentLocale);
    }

    if (typeof document !== 'undefined') {
      document.documentElement.lang = currentLocale;
    }

    if (typeof window !== 'undefined') {
      window.dispatchEvent(new CustomEvent(LOCALE_CHANGED_EVENT, { detail: currentLocale }));
    }
  },
  getLocale: () => currentLocale,
};
